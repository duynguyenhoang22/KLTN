"""
Build the phase-1 ViSmishDS dataset from validated real and synthetic sources.

This script creates a derived release file without modifying the source CSVs.
It keeps the agreed phase-1 metadata definitions and adds provenance columns so
later modeling stages can separate real and synthetic examples when needed.

Output:
    data/final/vismishds_phase1_final.csv
    data/reports/phase1_final_dataset_report.md

Run from repository root:
    python scripts/data_pipeline/build_phase1_final_dataset.py
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
import re

import pandas as pd


ROOT = Path(__file__).resolve().parents[2]
DATA_DIR = ROOT / "data"
GROUND_TRUTH_DIR = DATA_DIR / "ground_truth"
SYNTHETIC_DIR = DATA_DIR / "synthetic"
FINAL_DIR = DATA_DIR / "final"
REPORT_DIR = DATA_DIR / "reports"

FINAL_DATASET_PATH = FINAL_DIR / "vismishds_phase1_final.csv"
REPORT_PATH = REPORT_DIR / "phase1_final_dataset_report.md"


@dataclass(frozen=True)
class SourceSpec:
    dataset_name: str
    path: Path
    label: int
    origin: str
    category_column: str
    obfuscation_column: str | None = None


SOURCES = [
    SourceSpec(
        dataset_name="real_label_0",
        path=GROUND_TRUTH_DIR / "dataset_label_0_with_categories.csv",
        label=0,
        origin="real",
        category_column="category_label",
    ),
    SourceSpec(
        dataset_name="real_label_1",
        path=GROUND_TRUTH_DIR / "dataset_label_1_with_categories.csv",
        label=1,
        origin="real",
        category_column="smishing_type",
    ),
    SourceSpec(
        dataset_name="synthetic_label_0",
        path=SYNTHETIC_DIR / "synthetic_label_0.csv",
        label=0,
        origin="synthetic",
        category_column="category",
    ),
    SourceSpec(
        dataset_name="synthetic_label_1",
        path=SYNTHETIC_DIR / "synthetic_label_1.csv",
        label=1,
        origin="synthetic",
        category_column="category",
        obfuscation_column="level",
    ),
]

FINAL_COLUMNS = [
    "sample_id",
    "content",
    "label",
    "has_url",
    "has_phone_number",
    "sender_type",
    "category",
    "obfuscation_level",
    "data_origin",
    "source_dataset",
    "source_file",
    "source_row_id",
]
NO_OBFUSCATION_LEVEL = "NONE"


def read_csv(path: Path) -> pd.DataFrame:
    return pd.read_csv(path, encoding="utf-8-sig")


def normalize_content(value: object) -> str:
    if pd.isna(value):
        return ""
    return re.sub(r"\s+", " ", str(value).strip().lower())


def normalize_source(spec: SourceSpec) -> pd.DataFrame:
    df = read_csv(spec.path)
    required = {"content", "label", "has_url", "has_phone_number", "sender_type", spec.category_column}
    missing = sorted(required - set(df.columns))
    if missing:
        raise ValueError(f"{spec.path} is missing required columns: {', '.join(missing)}")

    out = pd.DataFrame(
        {
            "content": df["content"].astype(str).str.strip(),
            "label": pd.to_numeric(df["label"], errors="raise").astype(int),
            "has_url": pd.to_numeric(df["has_url"], errors="raise").astype(int),
            "has_phone_number": pd.to_numeric(df["has_phone_number"], errors="raise").astype(int),
            "sender_type": df["sender_type"].astype(str).str.strip(),
            "category": df[spec.category_column].astype(str).str.strip(),
            "obfuscation_level": NO_OBFUSCATION_LEVEL,
            "data_origin": spec.origin,
            "source_dataset": spec.dataset_name,
            "source_file": str(spec.path.relative_to(ROOT)).replace("\\", "/"),
            "source_row_id": df.index.astype(int),
        }
    )

    if spec.obfuscation_column:
        if spec.obfuscation_column not in df.columns:
            raise ValueError(f"{spec.path} is missing required column: {spec.obfuscation_column}")
        out["obfuscation_level"] = (
            df[spec.obfuscation_column].fillna(NO_OBFUSCATION_LEVEL).astype(str).str.strip()
        )
        out.loc[out["obfuscation_level"].eq(""), "obfuscation_level"] = NO_OBFUSCATION_LEVEL

    invalid_label_count = int(out["label"].ne(spec.label).sum())
    if invalid_label_count:
        raise ValueError(f"{spec.path} has {invalid_label_count} rows with label != {spec.label}")

    return out


def md_table(df: pd.DataFrame) -> str:
    if df.empty:
        return "_No rows._"
    return df.to_markdown(index=False)


def build_report(final_df: pd.DataFrame, source_counts: pd.DataFrame, duplicates_removed: int) -> str:
    lines: list[str] = []
    lines.append("# Phase 1 Final Dataset Report\n\n")
    lines.append("Generated by `scripts/data_pipeline/build_phase1_final_dataset.py`.\n\n")
    lines.append("## Output\n\n")
    dataset_path = str(FINAL_DATASET_PATH.relative_to(ROOT)).replace("\\", "/")
    lines.append(f"- Dataset: `{dataset_path}`\n")
    lines.append(f"- Rows: {len(final_df)}\n")
    lines.append(f"- Columns: {', '.join(FINAL_COLUMNS)}\n")
    lines.append(f"- Exact normalized duplicate rows removed: {duplicates_removed}\n")
    lines.append(f"- Non-applicable obfuscation level value: `{NO_OBFUSCATION_LEVEL}`\n\n")

    lines.append("## Source Inventory\n\n")
    lines.append(md_table(source_counts))
    lines.append("\n\n")

    label_counts = (
        final_df.groupby(["label", "data_origin"], dropna=False)
        .size()
        .reset_index(name="rows")
        .sort_values(["label", "data_origin"])
    )
    lines.append("## Label and Origin Counts\n\n")
    lines.append(md_table(label_counts))
    lines.append("\n\n")

    category_counts = (
        final_df.groupby(["label", "category"], dropna=False)
        .size()
        .reset_index(name="rows")
        .sort_values(["label", "rows"], ascending=[True, False])
    )
    lines.append("## Category Counts\n\n")
    lines.append(md_table(category_counts))
    lines.append("\n\n")

    level_counts = (
        final_df.loc[final_df["obfuscation_level"].ne(NO_OBFUSCATION_LEVEL)]
        .groupby("obfuscation_level", dropna=False)
        .size()
        .reset_index(name="rows")
        .sort_values("rows", ascending=False)
    )
    lines.append("## Synthetic Smishing Obfuscation Levels\n\n")
    lines.append(md_table(level_counts))
    lines.append("\n\n")

    lines.append("## Phase 1 Closure Notes\n\n")
    lines.append(
        "- Source CSVs are unchanged; this is a derived final dataset.\n"
        "- Synthetic `has_url` and `has_phone_number` metadata had zero validation mismatches before merging.\n"
        "- Real ground-truth metadata keeps the existing source annotations from phase 1.\n"
        "- Provenance columns allow later stages to train with synthetic data while keeping final test evaluation real-only.\n"
    )
    return "".join(lines)


def main() -> None:
    FINAL_DIR.mkdir(parents=True, exist_ok=True)
    REPORT_DIR.mkdir(parents=True, exist_ok=True)

    frames = [normalize_source(spec) for spec in SOURCES]

    merged = pd.concat(frames, ignore_index=True)
    merged["_content_norm"] = merged["content"].map(normalize_content)
    label_counts_by_content = merged.groupby("_content_norm")["label"].nunique()
    conflicting_duplicate_count = int(label_counts_by_content.gt(1).sum())
    if conflicting_duplicate_count:
        raise ValueError(
            f"Found {conflicting_duplicate_count} normalized duplicate content groups with conflicting labels"
        )

    before = len(merged)
    merged = merged.drop_duplicates(subset=["_content_norm"], keep="first").drop(columns="_content_norm")
    duplicates_removed = before - len(merged)

    merged.insert(0, "sample_id", [f"phase1_{idx:05d}" for idx in range(1, len(merged) + 1)])
    merged = merged[FINAL_COLUMNS]
    rows_after = merged["source_dataset"].value_counts().to_dict()
    source_counts = pd.DataFrame(
        {
            "source_dataset": [spec.dataset_name for spec in SOURCES],
            "source_file": [str(spec.path.relative_to(ROOT)).replace("\\", "/") for spec in SOURCES],
            "rows_before_dedup": [len(frame) for frame in frames],
            "rows_after_dedup": [rows_after.get(spec.dataset_name, 0) for spec in SOURCES],
        }
    )
    merged.to_csv(FINAL_DATASET_PATH, index=False, encoding="utf-8-sig")

    REPORT_PATH.write_text(build_report(merged, source_counts, duplicates_removed), encoding="utf-8")
    print(f"Wrote dataset: {FINAL_DATASET_PATH}")
    print(f"Wrote report: {REPORT_PATH}")
    print(f"Rows: {len(merged)}")
    print(f"Duplicates removed: {duplicates_removed}")


if __name__ == "__main__":
    main()
